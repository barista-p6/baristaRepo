import React from "react";
// for review
const V3iewMoreRecipeDetail = ({ reviews }) => {
  if (!reviews || reviews.length === 0) {
    return <p>No reviews available for this recipe.</p>;
  }

  return (
    <div>
      <h3>Reviews</h3>
      <ul>
        {reviews.map((review) => (
          <li key={review._id}>
            <p>User: {review.userId?.username || "Anonymous"}</p>
            <p>Barista: {review.baristaId?.username || "N/A"}</p>
            <p>Rating: {review.rating}</p>
            <p>Comment: {review.comment}</p>
          </li>
        ))}
      </ul>

    </div>
  );
};



export default V3iewMoreRecipeDetail;
